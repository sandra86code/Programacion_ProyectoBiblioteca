package com.jacaranda.recursos;

public class AutorException extends Exception {

	public AutorException() {
		super();
	}

	public AutorException(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
	}

	public AutorException(String message, Throwable cause) {
		super(message, cause);
	}

	public AutorException(String message) {
		super(message);
	}

	public AutorException(Throwable cause) {
		super(cause);
	}

}
