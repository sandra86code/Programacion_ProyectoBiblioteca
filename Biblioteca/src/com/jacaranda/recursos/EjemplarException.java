package com.jacaranda.recursos;

public class EjemplarException extends Exception {

	public EjemplarException() {
		super();
	}

	public EjemplarException(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
	}

	public EjemplarException(String message, Throwable cause) {
		super(message, cause);
	}

	public EjemplarException(String message) {
		super(message);
	}

	public EjemplarException(Throwable cause) {
		super(cause);
	}
	
}
