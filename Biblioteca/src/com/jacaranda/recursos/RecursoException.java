package com.jacaranda.recursos;

public class RecursoException extends Exception {

	public RecursoException() {
		super();
	}

	public RecursoException(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
	}

	public RecursoException(String message, Throwable cause) {
		super(message, cause);
	}

	public RecursoException(String message) {
		super(message);
	}

	public RecursoException(Throwable cause) {
		super(cause);
	}
	
}
